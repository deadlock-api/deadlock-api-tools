    scraped_at                     DateTime64(3) default now() CODEC (Delta, ZSTD),
    team0_core                     Bool alias bitAnd(objectives_mask_team0, bitShiftLeft(1, 0)) != 0,
    team0_tier1_lane1              Bool alias bitAnd(objectives_mask_team0, bitShiftLeft(1, 1)) != 0,

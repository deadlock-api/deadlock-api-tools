    team1_tier1_lane4              Bool alias bitAnd(objectives_mask_team1, bitShiftLeft(toUInt16(1), 7)) != 0,
    team1_tier2_lane4              Bool alias bitAnd(objectives_mask_team1, bitShiftLeft(toUInt16(1), 8)) != 0,
